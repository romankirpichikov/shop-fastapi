from sqlalchemy.orm import Session
from typing import List
from ..repositories.product_repository import ProductRepository
from ..repositories.category_repository import CategoryRepository
from ..schemas.product import ProductResponce, ProductListResponce, ProductCreate
from fastapi import HTTPException, status


class ProductService:
    def __init__(self, db: Session):
        self.product_repository = ProductRepository(db)
        self.category_repository = CategoryRepository(db)

    def get_all_products(self) -> ProductListResponce:
        products = self.product_repository.get_all()
        products_responce = [ProductResponce.model_validate(prod) for prod in products]
        return ProductListResponce(
            products=products_responce, total=len(products_responce)
        )

    def get_product_by_id(self, product_id: int) -> ProductResponce:
        product = self.product_repository.get_by_id(product_id)
        if not product:
            raise HTTPException(
                status_code=status.HTTP_404_NOT_FOUND,
                detail=f"Product with id {product_id} not found",
            )
        return ProductResponce.model_validate(product)

    def get_products_by_category(self, category_id: int) -> ProductListResponce:
        category = self.category_repository.get_by_id(category_id)
        if not category:
            raise HTTPException(
                status_code=status.HTTP_404_NOT_FOUND,
                detail=f"Category with id {category_id} not found",
            )
        products = self.product_repository.get_by_category(category_id)
        products_responce = [ProductResponce.model_validate(prod) for prod in products]
        return ProductListResponce(
            products=products_responce, total=len(products_responce)
        )

    def create_product(self, product_data: ProductCreate) -> ProductResponce:
        category = self.category_repository.get_by_id(product_data.category_id)
        if not category:
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST,
                detail=f"Category with id {product_data.category_id} does not exist",
            )
        product = self.product_repository.create(product_data)
        return ProductResponce.model_validate(product)
    